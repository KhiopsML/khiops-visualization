import { SaveService } from '../../app/khiops-covisualization/providers/save.service';
let appService: AppService;
let saveService: SaveService;
let treenodesService: TreenodesService;
let dimensionsDatasService: DimensionsDatasService;

describe('CoVisualization', () => {
  describe('Loading large coclustering [10.1.1_id_feat_nospace_Coclustering.json]', () => {
    beforeEach(() => {
      TestBed.configureTestingModule({
        imports: [TranslateModule.forRoot()],
  providers: [provideHttpClient()],
      });

      // Inject services
      dimensionsDatasService = TestBed.inject(DimensionsDatasService);
      treenodesService = TestBed.inject(TreenodesService);
      saveService = TestBed.inject(SaveService);
      appService = TestBed.inject(AppService);

      const fileDatas = require('../../assets/mocks/kc/10.1.1_id_feat_nospace_Coclustering.json');
      appService.setFileDatas(fileDatas);
    });

    it('Loading large coclustering: isBigJsonFile must return true', () => {
      const isBigJsonFile = appService.isBigJsonFile();
      expect(isBigJsonFile).toEqual(true);
    });

    it('Loading large coclustering: initial datas must be valid', () => {
      dimensionsDatasService.getDimensions();
      dimensionsDatasService.initSelectedDimensions();
      dimensionsDatasService.saveInitialDimension();
      dimensionsDatasService.constructDimensionsTrees();

      const dimensionsDatas: DimensionsDatasModel =
        dimensionsDatasService.getDatas();

      expect(dimensionsDatas.cellPartIndexes.length).toEqual(45057);
      expect(dimensionsDatas.cellPartIndexes[0]).toEqual([1201, 624]);
      expect(dimensionsDatas.currentDimensionsClusters[0]!.length).toEqual(
        5071,
      );
      expect(dimensionsDatas.currentDimensionsClusters[0]![0]!.bounds).toEqual(
        '{1040, 1719, 1720}',
      );
      expect(dimensionsDatas.currentDimensionsClusters[1]!.length).toEqual(
        1253,
      );
      expect(dimensionsDatas.currentDimensionsClusters[1]![0]!.bounds).toEqual(
        '{feat_1209}',
      );
      expect(dimensionsDatas.currentDimensionsTrees[0]![0]!.id).toEqual(2955);
      expect(dimensionsDatas.dimensions[0]!.initialParts).toEqual(2536);
    });

    it('Loading large coclustering: computed reduced datas must be valid', () => {
      dimensionsDatasService.getDimensions();
      dimensionsDatasService.initSelectedDimensions();
      dimensionsDatasService.saveInitialDimension();
      dimensionsDatasService.constructDimensionsTrees();

      const dimensionsDatas: DimensionsDatasModel =
        dimensionsDatasService.getDatas();

      const unfoldState =
        dimensionsDatas.dimensions.length *
        AppConfig.covisualizationCommon.UNFOLD_HIERARCHY.ERGONOMIC_LIMIT;

      treenodesService.setSelectedUnfoldHierarchy(unfoldState);
      const collapsedNodes = treenodesService.getLeafNodesForARank(unfoldState);
      treenodesService.setSavedCollapsedNodes(collapsedNodes);
      let datas = saveService.constructSavedJson(collapsedNodes);

      expect(datas.coclusteringReport.cellFrequencies.length).toEqual(214);
      expect(datas.coclusteringReport.cellPartIndexes.length).toEqual(214);
      expect(datas.coclusteringReport.cellPartIndexes[0]).toEqual([14, 13]);
      expect(datas.coclusteringReport.cellFrequencies[0]).toEqual(291203);
      expect(
        datas.coclusteringReport.dimensionHierarchies[0]!.clusters.length,
      ).toEqual(29);
      expect(
        datas.coclusteringReport.dimensionHierarchies[0]!.clusters![0]!
          .frequency,
      ).toEqual(74141);
      expect(
        datas.coclusteringReport.dimensionPartitions[0]!.valueGroups!.length,
      ).toEqual(15);
      expect(
        datas.coclusteringReport.dimensionPartitions[0]!.valueGroups![0]!
          .cluster,
      ).toEqual('A64');
      expect(datas.coclusteringReport.dimensionSummaries![0]!.parts).toEqual(
        15,
      );
      expect(datas.coclusteringReport.summary.cells).toEqual(214);
    });

    it('Loading large coclustering: computed reduced Matrix datas datas must be valid', () => {
      dimensionsDatasService.getDimensions();
      dimensionsDatasService.initSelectedDimensions();
      dimensionsDatasService.saveInitialDimension();
      dimensionsDatasService.constructDimensionsTrees();

      const dimensionsDatas: DimensionsDatasModel =
        dimensionsDatasService.getDatas();

      const unfoldState =
        dimensionsDatas.dimensions.length *
        AppConfig.covisualizationCommon.UNFOLD_HIERARCHY.ERGONOMIC_LIMIT;

      treenodesService.setSelectedUnfoldHierarchy(unfoldState);
      const collapsedNodes = treenodesService.getLeafNodesForARank(unfoldState);
      treenodesService.setSavedCollapsedNodes(collapsedNodes);
      const datasCollpased = saveService.constructSavedJson(collapsedNodes);
      appService.setCroppedFileDatas(datasCollpased);

      // Recompute with updated datas
      dimensionsDatasService.getDimensions();
      dimensionsDatasService.initSelectedDimensions();
      dimensionsDatasService.saveInitialDimension();
      dimensionsDatasService.constructDimensionsTrees();

      let matrixDatas = dimensionsDatasService.getMatrixDatas();

      expect(matrixDatas.matrixCellDatas!.length).toEqual(225);
      expect(matrixDatas.matrixCellDatas![0]!.cellFreq).toEqual(12080);
      expect(matrixDatas.matrixCellDatas![0]!.infosMutValue).toEqual([
        0.004023747674614358,
      ]);
    });

    it('Loading large coclustering with collapsed nodes: computed reduced datas must be valid', () => {
      dimensionsDatasService.getDimensions();
      dimensionsDatasService.initSelectedDimensions();
      dimensionsDatasService.saveInitialDimension();
      dimensionsDatasService.constructDimensionsTrees();

      const datasCollpased = saveService.constructSavedJson(collapsedNodes);
      appService.setCroppedFileDatas(datasCollpased);

      // Recompute with updated datas
      dimensionsDatasService.getDimensions();
      dimensionsDatasService.initSelectedDimensions();
      dimensionsDatasService.saveInitialDimension();
      dimensionsDatasService.constructDimensionsTrees();

      treenodesService.setSavedCollapsedNodes(collapsedNodes);

      saveService.constructSavedJson(collapsedNodes);

      const dimensionsDatas: DimensionsDatasModel =
        dimensionsDatasService.getDatas();

      expect(dimensionsDatas.cellPartIndexes.length).toEqual(26);
      expect(dimensionsDatas.cellPartIndexes[0]).toEqual([2, 3]);
      expect(dimensionsDatas.currentDimensionsClusters[0]!.length).toEqual(5);
      expect(dimensionsDatas.currentDimensionsClusters[0]![0]!.bounds).toEqual(
        'A64',
      );

      expect(dimensionsDatas.currentDimensionsTrees[0]![0]!.id).toEqual(4);
      expect(dimensionsDatas.dimensions[0]!.initialParts).toEqual(2536);
      expect(dimensionsDatas.dimensions[0]!.parts).toEqual(3);
    });
  });
});

